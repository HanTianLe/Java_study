package Java基础_01;

public class 关键字 {
    int i = 4;
    int j = 6;
    /**
     *public 表示公开的
     * class 表示定义一个类
     * Xuexi.Demo_01 表示一个类名
     */

    public static void main(String[] args) {  //表示定义一个公开状态主方法

        /**
         *public 表示公开的
         * static 表示静态的
         * void 表示空
         * main 表示方法名是main
         * (String[] args) 是一个main方法的形式参数列表
         */

        //字符串常量的输出
        System.out.println("Hello world!");
        //整数常量的输出
        System.out.println(10);
        System.out.println(22);
        //小数常量的输出
        System.out.println(100.10);
        System.out.println(10.0123);
        //字符常量的输出
        System.out.println('0');
        System.out.println('A');
        System.out.println('a');

//        System.out.println('ab');  //错误的！

        //布尔常量的输出
        System.out.println(true);
        System.out.println(false);
    }
}


